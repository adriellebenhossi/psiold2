// Função para obter ícone por nome
export function getIconByName(iconName: string): React.ComponentType<any> | null {
  // Procurar em todas as categorias
  for (const category of Object.values(SECTION_ICONS)) {
    if (category[iconName as keyof typeof category]) {
      return category[iconName as keyof typeof category] as React.ComponentType<any>;
    }
  }
  return null;
}

// Lista plana de todos os ícones para facilitar busca
export const ALL_ICONS = Object.entries(SECTION_ICONS).reduce((acc, [categoryName, icons]) => {
  Object.entries(icons).forEach(([iconName, IconComponent]) => {
    acc[iconName] = {
      name: iconName,
      component: IconComponent,
      category: categoryName
    };
  });
  return acc;
}, {} as Record<string, { name: string; component: React.ComponentType<any>; category: string }>);

